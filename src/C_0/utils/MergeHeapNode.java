package C_0.utils;

public class MergeHeapNode
{

    private int value;
    private int arrayNumber;

    public MergeHeapNode(int value, int arrayNumber)
    {
        this.value = value;
        this.arrayNumber = arrayNumber;
    }

    public int getValue()
    {
        return value;
    }

    public int getArrayNumber()
    {
        return arrayNumber;
    }
}

